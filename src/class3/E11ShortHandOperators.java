package class3;

public class E11ShortHandOperators {
    public static void main(String[] args) {

        int num = 100;
        num = num+1;
        num++; //increase the value of ver by 1
        double numb=15.5;
        numb++;
        System.out.println(numb);
        System.out.println(num);

        float f=11F;
        f/=2;
        System.out.println(f);

        int numm=13;
        numm%=4;
        System.out.println(numm);




    }
}
