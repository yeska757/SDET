package class2;

public class WholeNumbersDemo {
    public static void main(String[] args) {
        /*
        byte = data type
        age = variable
        50 = stored number
         */
        byte age = 50;
        short noOfApples = 300;
        int bankBalance = 9694200;
        long hugeNumber = 4206969898696969699L;
/*
in type long ---> add L at the end of number
 */


        System.out.println(age);
        System.out.println(noOfApples);
        System.out.println(bankBalance);
        System.out.println(hugeNumber);


    }
}
