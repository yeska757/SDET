public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        //float for floating decimal point
        float grocery = 20.5f;
        /* add f/F to float numbers
         */
        //double for long numbers with decimal
        double price = 42069696.83759837459;


        System.out.println(grocery);
        System.out.println(price);

    }
}
