package class3;

public class E4Devision {
    public static void main(String[] args) {
        System.out.println(10 / 5);
        //if the datat type is int we will not see decimal
        System.out.println(11 / 5);
        //if the data type will be double or float we will see decimal in answer
        System.out.println(11.0 / 5.0);
        int num1 = 10;
        int num2 = 4;
        double number1 = 10;
        double number2 = 4;
        System.out.println(num1 / num2);
        System.out.println(number1 / number2);


    }
}
