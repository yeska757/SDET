package class1;

public class VariablesDemo {


    public static void main(String[] args) {

        /* int + name ------> stores number related to that name */

        int age = 20;
        int n1 = 34646463;

        System.out.println(n1);
        System.out.println(age);

        /* String + name -------> stores text related to that name; */

        String name = "Zheka";
        System.out.println(name);


    }
}
