package class7;

public class E2WhileLoop {
    public static void main(String[] args) {

        int number = 0;
        //++ is an increment operayor
        number++;   // 0 -> 1
        number++;   // 1 -> 2
        System.out.println(number);

        //-- is a decrement operator
        int num2 = 10;
        num2--;
        System.out.println(num2);


    }
}
