package class7;

public class E14ForLoop {
    public static void main(String[] args) {

        int i =1;
        while(i<5){
            System.out.println(i);
            i++;
        }


        // where start; while; increment
        for(int j =1; j<5; j++){
            System.out.println(j);
        }














    }
}
