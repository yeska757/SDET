package class7;

public class E17DoWhile {
    public static void main(String[] args) {

        int i = 0;
        while (i > 10) {
            System.out.println(i);
            i++;
        }
// do while loop executes code once and then checks condition

        do {
            System.out.println(i);
        } while (i > 10);


    }
}
