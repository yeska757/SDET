package class3HW;

public class PositiveNegative {
    public static void main(String[] args) {
        int num1 = 365;
        if (num1 > 0) {
            System.out.println(num1 + " is a positive number.");
        } else if (num1 < 0) {
            System.out.println(num1 + " is a negative number.");
        } else {
            System.out.println("The number is 0.");
        }
    }
}
