package class8;

import java.util.Scanner;

public class E2WhileLoop {
    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);
        System.out.println("Enter number");
        int num = scan.nextInt();


        while (num != -1) {
            System.out.println("Try again");
            num = scan.nextInt();

        }















    }
}