package class6;

public class E7SwitchStatement {
    public static void main(String[] args) {

        String name = "Vlad";

        switch (name) {
            case "Vlad":
                System.out.println("Chicago");
                break;
            case "Jacob":
                System.out.println("Alabama");
                break;
            case "Axel":
                System.out.println("VA");
                break;

        }
    }
}
